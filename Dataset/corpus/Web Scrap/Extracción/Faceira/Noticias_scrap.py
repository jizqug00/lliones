import requests
from bs4 import BeautifulSoup
import json
import re

headers = {
    "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/123.0.0.0 Safari/537.36"
}

noticias = []
txt_contenido = []

# Define patrones típicos del asturleonés
def es_asturleones(texto):
    patrones = [
        r"\bd['lnt]\b",              # d', l', n'
        r"\bllion[eé]s\b",
        r"\bnues[uao]\b",
        r"\bconceyu\b",
        r"\bhestoria\b",
        r"\bn['']asturianu\b",
        r"\bllibru\b",
        r"\btamién\b",
    ]
    for patron in patrones:
        if re.search(patron, texto, re.IGNORECASE):
            return True
    return False

